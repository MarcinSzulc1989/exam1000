package pl.stormit;

public class Question {
    private String question;
    private String a;
    private String b;
    private String c;
    private String correctAnswer;

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }

    public String getCorrectAnswer() {
        return correctAnswer;
    }

    public String getQuestion() {
        return question;
    }
}
